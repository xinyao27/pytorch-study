"""配置参数"""

# ============ 数据配置 ============
VOCAB_SIZE = 10000   # 词表大小，保留最常见的 N 个字/词，超出的用 <UNK> 表示
MAX_LENGTH = 128     # 输入序列最大长度，超过截断，不足用 <PAD> 补齐
TRAIN_SIZE = 120000   # 训练集样本数量，越大模型学到的模式越多
TEST_SIZE = 2000     # 测试集样本数量，用于评估模型泛化能力

# ============ 模型配置 ============
D_MODEL = 256        # 嵌入维度，每个 token 用 256 维向量表示
N_HEADS = 4          # 注意力头数，多头注意力可并行关注不同特征
N_LAYERS = 4         # Transformer 层数，越深特征提取能力越强，但更难训练
D_FF = 512           # 前馈网络隐藏层维度，通常是 D_MODEL 的 2-4 倍
DROPOUT = 0.1        # Dropout 比例，随机丢弃 10% 神经元防止过拟合
NUM_CLASSES = 2      # 分类数量：0=消极，1=积极

# ============ 训练配置 ============
BATCH_SIZE = 32      # 批次大小，每次用 32 个样本计算梯度并更新参数
EPOCHS = 5           # 训练轮数，遍历整个数据集的次数
LR = 3e-4            # 学习率，控制参数更新步长，太大震荡，太小收敛慢
